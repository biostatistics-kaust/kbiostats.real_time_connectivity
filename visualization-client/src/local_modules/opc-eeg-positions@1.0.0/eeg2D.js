export const rescaledEEGLocations2D = ({ width, height, left, top }, invertY = true, zoom=0.9) => {
  const adjWidth = zoom * width;
  const adjHeight = zoom * height;
  const rescaled = {};
  Object.keys(EEG2DPositions).forEach(m => {
    const v = EEG2DPositions[m];
    rescaled[m] = [v[0] * adjWidth/2 + width / 2 + left, (invertY ? -1 : 1) * v[1] * adjHeight/2 + height / 2 + top];
  });
  return rescaled;
};
